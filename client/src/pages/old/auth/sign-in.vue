<template>
  <q-page class="flex flex-center">
    <q-card
      class="card-sign-in q-pa-md"
      inline
      color="white"
    >
      <q-card-section
        class="q-pa-md"
      >
        <img src="">
      </q-card-section>

      <q-card-section
        class="text-dark text-center"
      >
        Sign in to your account
      </q-card-section>

      <form @submit.prevent="signIn()">
        <q-card-section>
          <q-field
            icon="email"
            icon-color="light"
            class="q-mt-md"
          >
            <q-input
              v-model="form.email"
              placeholder="Email Address"
              type="email"
              autocomplete="username"
            />
          </q-field>

          <q-field
            icon="lock"
            icon-color="light"
            class="q-mt-lg"
          >
            <q-input
              v-model="form.password"
              placeholder="Password"
              type="password"
              autocomplete="current-password"
            />
          </q-field>
        </q-card-section>

        <q-card-actions
          align="center"
          class="q-mt-lg"
        >
          <q-btn
            label="Sign In"
            color="primary"
            size="large"
            type="submit"
          />
        </q-card-actions>
      </form>
    </q-card>
  </q-page>
</template>

<script>
export default {
  name: 'PageSignIn',
  data() {
    return {
      form: {
        email: '',
        password: '',
      },
    };
  },
  methods: {
    signIn() {
      const credentials = {
        email: this.form.email,
        password: this.form.password,
      };

      this.$store.dispatch('auth/signIn', credentials)
        .then(user => {
          this.$router.replace({ name: 'dashboard' }).catch(() => {});
        })
        .catch(error => {
          this.$q.notify('Invalid Login!');
          console.error(`Not signed in: ${error.message}`);
        });
    },
  },
};
</script>

